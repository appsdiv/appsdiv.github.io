let currentLang = 'fa';
let datepickerInstance;

const langSettings = {
    fa: { dir: 'rtl', locale: 'fa', datepicker: 'jalali' },
    en: { dir: 'ltr', locale: 'en', datepicker: 'gregorian' },
    tr: { dir: 'ltr', locale: 'tr', datepicker: 'gregorian' },
    ar: { dir: 'rtl', locale: 'ar', datepicker: 'gregorian' },
};

function setLanguage(lang) {
    currentLang = lang;
    const settings = langSettings[lang];

    document.documentElement.lang = lang;
    document.documentElement.dir = settings.dir;
    document.body.classList.remove('rtl', 'ltr');
    document.body.classList.add(settings.dir);
    
    // Update all text content, placeholders, and SEO tags
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    document.querySelectorAll('meta[name="description"]')[0].content = translations[lang]['seo-description'];
    document.querySelectorAll('meta[name="keywords"]')[0].content = translations[lang]['seo-keywords'];

    // Handle select options
    document.querySelectorAll('select option').forEach(option => {
        const key = option.getAttribute('data-translate');
        if (key && translations[lang][key]) {
            option.textContent = translations[lang][key];
        }
    });

    // Handle datepicker initialization
    initDatepicker(lang);
    
    // Update button icon direction
    document.querySelectorAll('.prev-button, .next-button, .submit-button').forEach(button => {
        const icon = button.querySelector('i');
        if (icon) {
            icon.classList.remove('ml-2', 'mr-2');
            icon.classList.add(settings.dir === 'rtl' ? 'ml-2' : 'mr-2');
        }
    });
    
    // Update radio button icon direction
    document.querySelectorAll('input[type="radio"]').forEach(radio => {
        radio.classList.remove('ml-2', 'mr-2');
        radio.classList.add(settings.dir === 'rtl' ? 'ml-2' : 'mr-2');
    });
}

function initDatepicker(lang) {
    const dateInput = document.getElementById('delivery-date');
    if (!dateInput) return;

    if (datepickerInstance && typeof datepickerInstance.destroy === 'function') {
        datepickerInstance.destroy();
    } else if (datepickerInstance) {
        $(dateInput).pDatepicker('destroy');
    }

    if (lang === 'fa') {
        datepickerInstance = $(dateInput).pDatepicker({
            format: 'YYYY/MM/DD',
            autoClose: true,
            initialValue: false,
            persianDigit: true,
            calendar: { persian: { locale: 'fa' } },
            toolbox: {
                today: { enabled: true, title: 'امروز' },
                calendar: { enabled: true, title: 'تقویم' },
                time: { enabled: false }
            }
        });
    } else {
        datepickerInstance = flatpickr(dateInput, {
            locale: lang,
            dateFormat: "Y-m-d",
        });
    }
}

function updateProgressBar(step) {
    const totalSteps = 3;
    const percentage = (step / totalSteps) * 100;
    document.querySelector('.progress-bar').style.width = percentage + '%';
}

function goToStep(step) {
    const currentStepElement = document.querySelector('.form-step:not(.hidden)');

    if (step === 2) {
        const customerName = document.getElementById('customer_name').value;
        const contactNumber = document.getElementById('contact_number').value;

        if (!customerName || !contactNumber) {
            Toastify({
                text: translations[currentLang]['toast-error-required-fields'],
                duration: 3000,
                gravity: "top",
                position: "right",
                backgroundColor: "#d9534f",
                stopOnFocus: true,
            }).showToast();
            return;
        }
    }

    if (step === 3) {
        const selectedProduct = document.getElementById('product-type').value;

        if (!selectedProduct) {
            Toastify({
                text: translations[currentLang]['toast-error-product-selection'],
                duration: 3000,
                gravity: "top",
                position: "right",
                backgroundColor: "#d9534f",
                stopOnFocus: true,
            }).showToast();
            return;
        }
    }

    document.querySelectorAll('.form-step').forEach(stepDiv => {
        stepDiv.classList.add('hidden');
    });

    if (step === 3) {
        const selectedProduct = document.getElementById('product-type').value;

        if (selectedProduct === 'alloy_ingot') {
            document.getElementById('step-3-alloy').classList.remove('hidden');
        } else if (selectedProduct === 'granule') {
            document.getElementById('step-3-granule').classList.remove('hidden');
        } else if (selectedProduct === 'wire') {
            document.getElementById('step-3-wire').classList.remove('hidden');
        } else if (selectedProduct === 'aac_conductor') {
            document.getElementById('step-3-aac').classList.remove('hidden');
        } else if (selectedProduct === 'aaac_conductor') {
            document.getElementById('step-3-aaac').classList.remove('hidden');
        } else if (selectedProduct === 'acsr_conductor') {
            document.getElementById('step-3-acsr').classList.remove('hidden');
        } else if (selectedProduct === 'abc_conductor') {
            document.getElementById('step-3-abc').classList.remove('hidden');
        } else if (selectedProduct === 'rod') {
            document.getElementById('step-3-rod').classList.remove('hidden');
        } else if (selectedProduct === 'aluminum_tray') {
            document.getElementById('step-3-aluminum-tray').classList.remove('hidden');
        }
        updateProgressBar(2);
    } else {
        const stepElement = document.getElementById('step-' + step);
        if (stepElement) {
            stepElement.classList.remove('hidden');
            updateProgressBar(step);
        }
    }
    setLanguage(currentLang);
}

